package com.jni.tool;

public class vedioChangeInfo {
    public int row = 0;
    public int angle = 0;
    public int col = 0;
    public int flag = 0;

    public void Setrow(int i) {
        this.row = i;
    }

    public int Getrow() {
        return this.row;
    }

    public void Setcol(int i) {
        this.col = i;
    }

    public int Getcol() {
        return this.col;
    }

    public void Setangle(int i) {
        this.angle = i;
    }

    public int Getangle() {
        return this.angle;
    }

    public void Setflag(int i) {
        this.flag = i;
    }

    public int Getflag() {
        return this.flag;
    }
}
